# Sort an array in ascending and descending order

nums = [40, 10, 50, 30, 20]

# Ascending sort
asc = sorted(nums)
print("Ascending order:", asc)

# Descending sort
desc = sorted(nums, reverse=True)
print("Descending order:", desc)



# output:
# Ascending order: [10, 20, 30, 40, 50]
# Descending order: [50, 40, 30, 20, 10]
